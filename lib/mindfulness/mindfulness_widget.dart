      logFirebaseEvent('MINDFULNESS_Mindfulness_ON_LOAD');
      logFirebaseEvent('Mindfulness_revenue_cat');
      final isEntitled = await revenue_cat.isEntitled('Annual_access');
      if (isEntitled == null) {
        return;
      } else if (!isEntitled) {
        await revenue_cat.loadOfferings();
      }

      if (isEntitled) {
        logFirebaseEvent('Mindfulness_navigate_to');

        context.pushNamed('Mindfulness_annual');

        return;
      } else {
        logFirebaseEvent('Mindfulness_revenue_cat');
